package com.a531tracker.LiftBuilders;

public class AccessoryLifts {
    private String accessory_name;
    private String accessory_reps_and_sets;
    private float accessory_weight;

    public String getAccessory_name() {
        return accessory_name;
    }

    public void setAccessory_name(String accessory_name) {
        this.accessory_name = accessory_name;
    }

    public String getAccessory_reps_and_sets() {
        return accessory_reps_and_sets;
    }

    public void setAccessory_reps_and_sets(String accessory_reps_and_sets) {
        this.accessory_reps_and_sets = accessory_reps_and_sets;
    }

    public float getAccessory_weight() {
        return accessory_weight;
    }

    public void setAccessory_weight(float accessory_weight) {
        this.accessory_weight = accessory_weight;
    }
}
