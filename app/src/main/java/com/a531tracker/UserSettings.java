package com.a531tracker;

public class UserSettings {
    private int weekFormat;
    private int chosenBBBFormat;
    private float chosenBBBPercent;
    private int swapBBBFormat;



    public int getWeekFormat() {
        return weekFormat;
    }

    public void setWeekFormat(int weekFormat) {
        this.weekFormat = weekFormat;
    }

    public int getChosenBBBFormat() {
        return chosenBBBFormat;
    }

    public void setChosenBBBFormat(int chosenBBBFormat) {
        this.chosenBBBFormat = chosenBBBFormat;
    }

    public float getChosenBBBPercent() {
        return chosenBBBPercent;
    }

    public void setChosenBBBPercent(float chosenBBBPercent) {
        this.chosenBBBPercent = chosenBBBPercent;
    }

    public int getSwapBBBFormat() {
        return swapBBBFormat;
    }

    public void setSwapBBBFormat(int swapBBBFormat) {
        this.swapBBBFormat = swapBBBFormat;
    }
}
